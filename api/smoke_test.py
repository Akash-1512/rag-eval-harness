"""
api/smoke_test.py

First real end-to-end test: question -> retrieve -> LLM -> answer.
Uses real questions from our Q&A pairs against real paper chunks.

Usage:
    python -m api.smoke_test

Expected output:
    - 3 real answers generated by Groq llama-3.1-70b
    - Each answer grounded in retrieved context
    - No hallucinations visible on manual inspection
    - Metadata showing which papers were cited
"""

from loguru import logger
from dotenv import load_dotenv

from ingestion.document_loader import load_all_papers
from ingestion.chunker import ChunkingStrategy, chunk_documents
from retrieval.vector_store import build_index
from api.rag_pipeline import run_rag

load_dotenv()

# Real Q&A pairs from our corpus — question + ground truth for manual verification
TEST_QA = [
    {
        "question": "How many attention heads does the Transformer base model use, and what is the dimensionality of each head?",
        "ground_truth": "The base Transformer uses 8 attention heads. The model dimensionality is 512, so each head has a dimensionality of 64 (512/8).",
    },
    {
        "question": "What are the two RAG formulations introduced in the original RAG paper?",
        "ground_truth": "RAG-Sequence conditions the full answer on a single retrieved document, and RAG-Token can draw from different documents for each generated token.",
    },
    {
        "question": "According to the RAGAS paper, what does the faithfulness metric specifically measure?",
        "ground_truth": "Faithfulness measures whether all claims made in the generated answer can be inferred from the provided context. It is computed as the ratio of supported claims to total claims in the answer.",
    },
]


def run_smoke_test():
    logger.info("=" * 60)
    logger.info("RAG PIPELINE SMOKE TEST — first real LLM calls")
    logger.info("=" * 60)

    # Build index from 3 papers
    logger.info("\nStep 1: Loading papers and building index")
    docs = load_all_papers(paper_ids=[
        "01_attention_is_all_you_need",
        "06_rag",
        "08_ragas",
    ])
    chunks = chunk_documents(docs, strategy=ChunkingStrategy.RECURSIVE)
    vector_store = build_index(chunks, strategy_name="recursive_smoke")
    logger.success(f"  Index ready: {len(chunks)} chunks")

    # Run RAG on each test question
    logger.info("\nStep 2: Running RAG pipeline on 3 real questions")

    for i, qa in enumerate(TEST_QA, 1):
        logger.info(f"\n{'='*50}")
        logger.info(f"Question {i}: {qa['question']}")
        logger.info(f"{'='*50}")

        output = run_rag(
            question=qa["question"],
            vector_store=vector_store,
            ground_truth=qa["ground_truth"],
            top_k=5,
        )

        logger.info(f"\nGENERATED ANSWER:\n{output.answer}")
        logger.info(f"\nGROUND TRUTH:\n{qa['ground_truth']}")
        logger.info(f"\nSOURCES: {list(zip(output.metadata['sources'], output.metadata['pages']))}")

        # Manual faithfulness check
        logger.info("\nMANUAL CHECK — does the answer:")
        logger.info("  1. Match the ground truth in substance?")
        logger.info("  2. Add any claims NOT in the ground truth? (hallucination risk)")
        logger.info("  3. Miss any claims FROM the ground truth? (incompleteness risk)")

    logger.info(f"\n{'='*60}")
    logger.success("RAG smoke test complete — review answers above manually")
    logger.info("These exact outputs will be scored by RAGAS in Milestone 5")
    logger.info("=" * 60)


if __name__ == "__main__":
    run_smoke_test()
